      <h5>MetroBus (Autocarro de metro)</h5>
      <p>
        A rede MetroBus em Berlim inclui vários autocarros expressos que param nas paragens mais importantes do
        centro urbano de Berlim. Pode reconhecer essas linhas pela letra M. As linhas de MetroBus M11
        para M85 funcionam 24 horas por dia, sete dias por semana, em intervalos de dez minutos.
      </p>

      <h5>Autocarros noturnos</h5>
      <p>
        As linhas de autocarros noturnos são marcadas com a letra N e circulam a noite toda. As linhas N1 a N9 substituem as
        linhas de metro U1 a U9 à noite de segunda a sexta-feira. Os autocarros noturnos N10 a N97 substituem as mais
        importantes linhas de autocarros diurnos.
      </p>
