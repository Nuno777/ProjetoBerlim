      <img src="assets/img/273px-S-Bahn_Berlin_Baureihe_481.jpg" alt="image of the S-Bahn">

      <h5>Sobre o S-Bahn de Berlim</h5>
      <p>O S-Bahn é uma rede ferroviária que cobre 15 linhas e quase 170 estações. Os 330 quilômetros de
        rede regional cobre o centro da cidade e a área suburbana de Berlim. No centro de Berlim, o S-Bahn passa
        principalmente acima do solo. As estações S-Bahn podem ser facilmente
        identificadas pelo símbolo S verde e branco.
      </p>

      <h5>Horários</h5>
      <p>
        Durante a semana, o S-Bahn começa a funcionar por volta das 4h30 e interrompe o serviço à 1h30. Dependendo da
        hora do dia, os comboios circulam em intervalos de cinco, dez ou 20 minutos. Nos fins de semana, o S-Bahn circula 24
        horas, à noite em intervalos de 30 minutos.
      </p>
    </div>

